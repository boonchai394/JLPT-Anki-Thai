def parse_data():
    rows = []
    for line in RAW.splitlines():
        k, s, on, kun, meaning, pos, memory = line.split("|")
        vocab, examples = SPECIAL.get(
            k,
            (
                [(k, kun.split("・")[0] if kun != "—" else on.split("・")[0],
                  pos, meaning)],
                [(f"{k}を勉強します。", f"{k} を べんきょうします。", f"เรียนคันจิ {k}")]
            )
        )
        rows.append({
            "kanji": k,
            "strokes": s,
            "on": on,
            "kun": kun,
            "meaning": meaning,
            "pos": pos,
            "memory": memory,
            "vocab": vocab,
            "examples": examples,
        })
    return rows

